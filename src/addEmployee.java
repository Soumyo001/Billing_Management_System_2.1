    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name=jTextField1.getText();
        String id=jTextField2.getText();
        String contactNo=jTextField3.getText();
        String age=jTextField4.getText();
        String address=jTextField5.getText();
        String gender=(String)jComboBox1.getSelectedItem();
        try{
            Statement st=connectSQL.getConnection().createStatement();
            ResultSet rs=st.executeQuery("select*from buyer where contactNo='"+contactNo+"'");
            if(rs.next()) throw new billing_management_system.sameDataException();
            rs=st.executeQuery("select*from employee where id='"+id+"'");
            if(rs.next())throw new billing_management_system.sameDataException();
            if(name.equals("Enter Employee Name")||id.equals("Enter Employee ID")||contactNo.equals("Enter Employee Contact No")||age.equals("Enter Employee Age")||address.equals("Enter Employee Address")||gender.equals(null))
                throw new billing_management_system.emptyFieldException();
            st.executeUpdate("insert into employee values('"+name+"','"+id+"','"+age+"','"+contactNo+"','"+address+"','"+gender+"')");
            JOptionPane.showMessageDialog(null,"Successfully Saved!");
            setVisible(false);
            new addEmployee().setVisible(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

